public class SurfaceArea{
    public static void surfaceArea(int radious){
        System.out.println(radious * radious * 3.14);
    }
    
    public static void surfaceArea(int heigth, int length){
        System.out.println(heigth * length);
    }
    
    public static void surfaceAreaTriangle(int base, int heigth){
        System.out.println(base * heigth * 0.5);
    }
    
}